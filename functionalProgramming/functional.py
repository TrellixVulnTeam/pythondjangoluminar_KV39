# functional programming is used to reduce the length of the code
# lamda functions
add = lambda num1, num2: num1 + num2
print(add(10, 30))
sub = lambda num1, num2: num1 - num2
print(sub(30, 10))
cube = lambda num: num ** 3
print(cube(2))
even = lambda num: num % 2 == 0
print(even(20))
